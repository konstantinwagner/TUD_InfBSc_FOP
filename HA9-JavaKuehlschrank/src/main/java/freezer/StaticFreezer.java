package freezer;

/**
 * Static class to define available Freezer sizes
 *
 * @author Nicolas Weber
 */
class StaticFreezer {

    /**
     * Contains all available widths of a freezer configurations
     */
    final static double[] availableWidths = new double[]{0.5, 1.0};

    /**
     * Contains all available heights configurations
     */
    final static double[] availableHeights = new double[]{1.0, 2.0};

    /**
     * Contains all available depths configurations
     */
    final static double[] availableDepths = new double[]{0.5, 1.0, 1.5};

    /**
     * Contains all available wall thickness configurations
     */
    final static double[] availableWallThickness = new double[]{0.01, 0.1};
}